char isWall(int x, int y)
{
    if(walls[y][x] == '#'){
        return 1;
    }
    return 0;
}

int getPoint(int x, int y)
{
    for(int i =0;i<244;i++){
        Food * fs = &f[i];
        if(!fs->hidden && fs->x == x && fs->y == y){
            fs->hidden = 1;
            return fs->point;
        }
    }
    return 0;
}

char isIntersection(int x, int y)
{
    for(int i =0;i<64;i++){
        Intersection i_aux = inter[i];
        if(i_aux.x == x && i_aux.y == y){
            return 1;
        }
    }
    return 0;
};

